#!/bin/bash

# Function to recursively delete all node_modules directories
delete_node_modules() {
    local directory="$1"

    # Check if the directory exists
    if [ -d "$directory" ]; then
        echo "Deleting node_modules directories in: $directory"
        # Find all directories named "node_modules" and delete them
        find "$directory" -type d -name "node_modules" -exec rm -rf {} +
        echo "Deleted node_modules directories."
    else
        echo "Directory '$directory' not found."
    fi
}

# Default to the current directory if no argument is provided
directory="${1:-.}"

# Example usage: call the function with the directory path
delete_node_modules "$directory"
