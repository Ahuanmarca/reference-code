def main():
    # Check for correct number of arguments
    if len(sys.argv) != 2:
        print("Incorrect Input")
        return

    # Check if file exists
    filename = sys.argv[1]
    path = "./" + filename
    if os.path.isfile(path) == True:
        print("File already exists")
        return

    fields = ["Number", "name", "species", "breed", "gender", "birth", "chipNumber"]

    # Generate random pets csv file
    print("Generating random pets csv file")
    with open(filename, "w") as csvfile:
        writer = csv.DictWriter(csvfile, fieldnames=fields)
        writer.writeheader()
        for num in range(600):
            gender = random.choice(["female", "male"])
            name = random.choice(names[gender])
            species = random.choice(["dog", "cat"])
            breed = random.choice(breeds[species])

            newRow = {
                "Number": num + 1,
                "name": name,
                "species": species,
                "breed": breed,
                "gender": gender,
                "birth": random_date_between_years(2010, 2023),
                "chipNumber": generate_random_chip_number(),
            }

            writer.writerow(newRow)


def random_date_between_years(start_year, end_year):
    start_date = datetime(start_year, 1, 1)
    end_date = datetime(end_year, 12, 31)

    days_difference = (end_date - start_date).days
    random_days = random.randint(0, days_difference)

    random_date = start_date + timedelta(days=random_days)
    return random_date.strftime("%Y-%m-%d")


def generate_random_chip_number():
    first = random.choice("ABCDEFGHIJKLMNOPQRSTUVWXYZ")
    second = random.choice("MNOP")
    middle = str(random.randint(100000, 999999))
    last = random.choice("WXYZ")
    return first + second + middle + last


